const jwt = require('jsonwebtoken');
const User = require('../models/User');

// Generate JWT Token
const generateToken = (id) => {
  return jwt.sign({ id }, process.env.JWT_SECRET || 'supersecretkeymedconnect123!', {
    expiresIn: '30d',
  });
};

// @desc    Register user
// @route   POST /api/auth/register
// @access  Public
const registerUser = async (req, res) => {
  const { name, email, password, role, phone, specialization, experience, bio, bloodGroup, medicalHistory } = req.body;

  try {
    // Check if user exists
    const userExists = await User.findOne({ email });

    if (userExists) {
      return res.status(400).json({ success: false, message: 'User already exists' });
    }

    // Create user object
    const userFields = {
      name,
      email,
      password,
      role: role || 'patient',
      phone,
    };

    // Add role-specific details
    if (role === 'doctor') {
      userFields.specialization = specialization || '';
      userFields.experience = experience || 0;
      userFields.bio = bio || '';
      userFields.isVerified = false; // Requires admin verification
      
      // Seed some initial slots for ease of booking
      userFields.availability = [
        "Monday 09:00", "Monday 10:00", "Monday 11:00",
        "Wednesday 14:00", "Wednesday 15:00", "Wednesday 16:00",
        "Friday 09:00", "Friday 10:00", "Friday 11:00"
      ];
    } else if (role === 'patient') {
      userFields.bloodGroup = bloodGroup || '';
      userFields.medicalHistory = medicalHistory || '';
    } else if (role === 'admin') {
      // Direct setup for testing, could be auto-verified
      userFields.isVerified = true;
    }

    const user = await User.create(userFields);

    if (user) {
      res.status(201).json({
        success: true,
        data: {
          _id: user._id,
          name: user.name,
          email: user.email,
          role: user.role,
          token: generateToken(user._id),
        },
      });
    } else {
      res.status(400).json({ success: false, message: 'Invalid user data' });
    }
  } catch (error) {
    console.error('Registration error:', error);
    res.status(500).json({ success: false, message: error.message });
  }
};

// @desc    Authenticate user & get token
// @route   POST /api/auth/login
// @access  Public
const loginUser = async (req, res) => {
  const { email, password } = req.body;

  try {
    // Find user
    const user = await User.findOne({ email });

    if (!user) {
      return res.status(401).json({ success: false, message: 'Invalid email or password' });
    }

    // Check if password matches
    const isMatch = await user.comparePassword(password);

    if (!isMatch) {
      return res.status(401).json({ success: false, message: 'Invalid email or password' });
    }

    res.json({
      success: true,
      data: {
        _id: user._id,
        name: user.name,
        email: user.email,
        role: user.role,
        phone: user.phone,
        isVerified: user.isVerified,
        token: generateToken(user._id),
      },
    });
  } catch (error) {
    console.error('Login error:', error);
    res.status(500).json({ success: false, message: error.message });
  }
};

// @desc    Get current user profile
// @route   GET /api/auth/me
// @access  Private
const getMe = async (req, res) => {
  try {
    const user = await User.findById(req.user.id).select('-password');
    if (!user) {
      return res.status(404).json({ success: false, message: 'User not found' });
    }
    res.json({ success: true, data: user });
  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
};

module.exports = {
  registerUser,
  loginUser,
  getMe,
};
